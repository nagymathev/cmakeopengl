Cube::Cube()
    : vao{ cube_vertices }, shader{ nullptr }, model{ glm::translate(model, position) }
{
    vao.AddAttribute(3);
    vao.AddAttribute(3);
    vao.AddAttribute(2);
}

Cube::Cube(Shader *shader)
    : vao{ cube_vertices }, shader{ shader }, model{ glm::translate(model, position) }
{
    vao.AddAttribute(3);
    vao.AddAttribute(3);
    vao.AddAttribute(2);
}

Cube::~Cube()
{
}

void Cube::update(float delta_time)
{
    vao.Bind();
    shader->use();

    apply_transformations();
    angle += delta_time * 5;

    shader->setMat4("model", model);
}

void Cube::apply_transformations()
{
    model = glm::mat4(1.f);
    model = glm::translate(model, position);

    model = glm::rotate(model, glm::radians(angle), rotation_angle);
    model = glm::scale(model, scale);
}

void Cube::set_position(glm::vec3 _position)
{
    position = _position;
    apply_transformations();
}

void Cube::set_rotation(float _angle)
{
    angle = _angle;
    apply_transformations();
}

void Cube::set_rotation_angle(glm::vec3 _direction)
{
    rotation_angle = _direction;
}
